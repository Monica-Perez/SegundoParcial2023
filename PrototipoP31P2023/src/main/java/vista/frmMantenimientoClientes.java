    public void llenadoDeTablas() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("ID");
        modelo.addColumn("Nombre");
        modelo.addColumn("Nit");
        modelo.addColumn("Debe");
        modelo.addColumn("Haber");
        clsClientes aplicacion = new clsClientes();
        //VendedorDAO vendedorDAO = new VendedorDAO();
        List<clsClientes> listaAplicaciones = aplicacion.getListadoAplicaciones();
        tablaUsuarios.setModel(modelo);
        String[] dato = new String[5];
        for (int i = 0; i < listaAplicaciones.size(); i++) {
            dato[0] = Integer.toString(listaAplicaciones.get(i).getIdCliente());
            dato[1] = listaAplicaciones.get(i).getNombreCliente();
            dato[2] = listaAplicaciones.get(i).getNitCliente();
            dato[3] = listaAplicaciones.get(i).getDebeCliente();
            dato[4] = listaAplicaciones.get(i).getHaberCliente();
            modelo.addRow(dato);
        }       
    }
